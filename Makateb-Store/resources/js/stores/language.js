// Function to translate category/package names
export const translateName = (name, type = 'category') => {
  if (!name) return name;
  const lang = localStorage.getItem('language') || 'ar';
  if (lang !== 'ar') return name;

  const translations = type === 'category' ? categoryTranslations : packageTranslations;
  const nameLower = name.toLowerCase().trim();

  // Direct match
  if (translations[nameLower]) {
    return translations[nameLower];
  }

  // Partial match (contains)
  for (const [en, ar] of Object.entries(translations)) {
    if (nameLower.includes(en) || en.includes(nameLower)) {
      return ar;
    }
  }

  return name; // Return original if no translation found
};

