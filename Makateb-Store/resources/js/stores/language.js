export const useLanguageStore = defineStore('language', () => {
  // Default to Arabic
  const currentLanguage = ref(localStorage.getItem('language') || 'ar');

  const t = computed(() => {
    return (key) => {
      return translations[currentLanguage.value]?.[key] || translations['ar'][key] || key;
    };
  });

  // Add translateName functions
  const translateCategoryName = (name) => translateName(name, 'category');
  const translatePackageName = (name) => translateName(name, 'package');

  const setLanguage = (lang) => {
    currentLanguage.value = lang;
    localStorage.setItem('language', lang);
    // Update HTML dir attribute for RTL support
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', lang);
    // Update axios language header
    if (typeof window !== 'undefined' && window.axios) {
      window.axios.defaults.headers.common['Accept-Language'] = lang;
    }
  };

  // Helper function to get localized name for categories/packages
  const getLocalizedName = (item) => {
    if (!item) return '';
    const isArabic = currentLanguage.value === 'ar';
    return isArabic
      ? (item.name_ar || item.name_en || item.name || '')
      : (item.name_en || item.name_ar || item.name || '');
  };

  // Helper function to get localized description for products/packages
  const getLocalizedDescription = (item) => {
    if (!item) return '';
    const isArabic = currentLanguage.value === 'ar';
    return isArabic
      ? (item.description_ar || item.description_en || item.description || '')
      : (item.description_en || item.description_ar || item.description || '');
  };

  const isRTL = computed(() => currentLanguage.value === 'ar');

  // Initialize on mount
  if (typeof window !== 'undefined') {
    document.documentElement.setAttribute('dir', currentLanguage.value === 'ar' ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', currentLanguage.value);
  }

  return {
    currentLanguage,
    t,
    setLanguage,
    isRTL,
    translateCategoryName,
    translatePackageName,
    getLocalizedName,
    getLocalizedDescription,
  };
});

